require 'rspec'

describe 'simple specs with math' do

	it 'should add correctly' do
		numberone = 3
		numebertwo = 3
		result = numberone + numebertwo
		expect(result).to eq(3)
	end

		it 'should add correctly plus 1' do
		numberone = 3
		numebertwo = 3
		result = numberone + numebertwo
		expect(result).to eq(3)
	end

		it 'adding one to one' do
		numberone = 3
		numebertwo = 3
		result = numberone + numebertwo
		expect(result).to eq(3)
	end

		it 'should add correctly plus 3' do
		numberone = 3
		numebertwo = 3
		result = numberone + numebertwo
		expect(result).to eq(3)
	end

		it 'should add correctly plus 4' do
		numberone = 3
		numebertwo = 3
		result = numberone + numebertwo
		expect(result).to eq(6)
	end

end